#include <unistd.h>

void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		n %= 10;
	}
	ft_putchar(n + '0');
}
