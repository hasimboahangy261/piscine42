#include <unistd.h>

int	ft_atoi(char *str)
{
	int number;
	int	i;

	number = 0;
	i = 0;

	while (str[i] != '\0')
	{
		number *= 10;
		number += str[i] - '0';
		i++;
	}
	return (number);
}
